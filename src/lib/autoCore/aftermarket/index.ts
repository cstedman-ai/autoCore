/** Look up an aftermarket manufacturer by its (case-insensitive) name. */
export function getAftermarketManufacturer(
  name: string,
): AftermarketManufacturer | undefined {
  const n = name.toLowerCase();
  return aftermarketManufacturers.find((m) => m.name.toLowerCase() === n);
}

/** All manufacturers that make parts for a given vehicle brand. */
export function getAftermarketByBrand(brand: string): AftermarketManufacturer[] {
  const b = brand.toLowerCase();
  return aftermarketManufacturers.filter((m) =>
    m.brandSpecializations.some((s) => s.toLowerCase() === b),
  );
}
